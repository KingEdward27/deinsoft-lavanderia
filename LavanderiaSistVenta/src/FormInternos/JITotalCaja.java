    private void VerDatos() throws SQLException, ClassNotFoundException {
        try {
            int filas = jTable1.getRowCount();
            for (int i = 0; i < filas; i++) {
                dtm1.removeRow(0);
            }
            String fec1 = Formatos.sdfFecha.format(jdprFecha1.getDate());
            String fec2 = Formatos.sdfFecha.format(jdprFecha2.getDate());
            Date fechasql1 = Formatos.FechaSQL(fec1);
            Date fechasql2 = Formatos.FechaSQL(fec2);
            String est="1";
            for (ConsultaVentas la : VentasADN.ConsultaVentasTC(fechasql1,fechasql2,est)) {
                dtm1.addRow(la.ArregloDatosTC());
            }
            //tabla ingresos
            filas = jTable2.getRowCount();
            for (int i = 0; i < filas; i++) {
                dtm2.removeRow(0);
            }
            fec1 = Formatos.sdfFecha.format(jdprFecha1.getDate());
            fechasql1 = Formatos.FechaSQL(fec1);
            fec2 = Formatos.sdfFecha.format(jdprFecha2.getDate());
            fechasql2 = Formatos.FechaSQL(fec2);
            for (Ingresos la : IngresosADN.Lista(fechasql1,fechasql2)) {
                dtm2.addRow(la.ArregloDatos());
            }
            //tabla egresos
            filas = jTable3.getRowCount();
            for (int i = 0; i < filas; i++) {
                dtm3.removeRow(0);
            }
            fec1 = Formatos.sdfFecha.format(jdprFecha1.getDate());
            fechasql1 = Formatos.FechaSQL(fec1);
            fec2 = Formatos.sdfFecha.format(jdprFecha2.getDate());
            fechasql2 = Formatos.FechaSQL(fec2);
            for (Egresos la : EgresosADN.Lista(fechasql1,fechasql2)) {
                dtm3.addRow(la.ArregloDatos());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.toString());
        }
    }
