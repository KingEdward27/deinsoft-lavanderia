    private void jbtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnGuardarActionPerformed
        try {
            String nom = "", clave1 = "", clave2 = "", tipo = "";
            nom = jtfdNick.getText().trim().toUpperCase();
            clave1 = jtfdClave1.getText().trim();
            clave2 = jtfdClave2.getText().trim();
            tipo = jcbxTipo.getSelectedItem().toString().toUpperCase();
            Usuarios cli = null;
            String mensaje = "";
            if (nom.length() == 0) {
                JOptionPane.showMessageDialog(rootPane, "Asegurese de llenar los datos correctamente");
            } else {
                if (clave1 == null ? clave2 != null : !clave1.equals(clave2)) {
                    JOptionPane.showMessageDialog(rootPane, "Las claves no coinciden");
                } else {
                    Usuarios c = new Usuarios();
                c.setNombre(nom);
                c.setIdUsuarios(idcat);
                if (modif == false) {
                    cli = new Usuarios(0, nom, clave2, tipo, "1");
                    mensaje = "Datos guardados correctamente";
                } else {
                    cli = new Usuarios(idcat, nom, clave2, tipo, est);
                    mensaje = "Datos actualizados correctamente";
                }
                if (UsuariosADN.Guardar(cli)) {
                    JOptionPane.showMessageDialog(rootPane, mensaje);
                    VerDatos();
                    jbtnNuevoActionPerformed(evt);
//                    }
                }
                }
                
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.toString());
        }
    }//GEN-LAST:event_jbtnGuardarActionPerformed

