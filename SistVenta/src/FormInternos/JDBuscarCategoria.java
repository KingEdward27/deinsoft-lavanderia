    private void VerDatos() throws SQLException, ClassNotFoundException {
        try {
            int filas = jteLista.getRowCount();
            for (int i = 0; i < filas; i++) {
                dtm.removeRow(0);
            }
            Categorias c = new Categorias();
            c.setNom(jtfdCategoriaB.getText().trim());
            c.setEstado(String.valueOf(estado));
            for (Categorias la : CategoriasADN.Lista(c)) {
                dtm.addRow(la.ArregloDatos());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.toString());
        }

    }

    private void EnviarDatos() throws NumberFormatException {
        int fila = jteLista.getSelectedRow();
        modif = true;
        if (fila >= 0) {
            if (JIProductos.llamada) {
                JIProductos.categoria.setIdcategoria(Integer.parseInt(jteLista.getValueAt(fila,0).toString()));
                JIProductos.categoria.setNom(jteLista.getValueAt(fila,1).toString());
            }
            dispose();
        }
    }
