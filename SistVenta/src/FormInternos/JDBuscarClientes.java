    private void VerDatos() throws SQLException, ClassNotFoundException {
        try {
            int filas = jteLista.getRowCount();
            for (int i = 0; i < filas; i++) {
                dtm.removeRow(0);
            }
            Clientes c = new Clientes();
            c.setDni(jtfdDni.getText().trim());
            c.setNombres(jtfdBuscarNombres.getText().trim());
            c.setEstado(String.valueOf(estado));
            for (Clientes la : ClientesADN.Lista(c)) {
                dtm.addRow(la.ArregloDatos());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.toString());
        }

    }

    private void EnviarDatos() throws NumberFormatException {
        int fila = jteLista.getSelectedRow();
        if (fila >= 0) {
            JIVentas.idcli = Integer.parseInt(jteLista.getValueAt(fila, 0).toString());
            JIVentas.nomcli = (jteLista.getValueAt(fila, 1).toString());
            dispose();
        }
    }
